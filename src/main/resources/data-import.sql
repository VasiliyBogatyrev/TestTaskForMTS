
DROP SEQUENCE IF EXISTS INVOICE_SEQ;
CREATE SEQUENCE IF NOT EXISTS INVOICE_SEQ START WITH 1 INCREMENT BY 1;

DROP TABLE IF EXISTS INVOICE;
CREATE TABLE IF NOT EXISTS INVOICE(
  ID INT PRIMARY KEY,
  NAME VARCHAR(20),
  TIN VARCHAR(10),
  СPP VARCHAR(10),
  PRODUCT VARCHAR(30),
  QUANTITY INT
);

